def print_top_arrow():
    print(" /\\")
    print("/  \\")
    print()

def print_mid_arrow():
    print(" ||")
    print(" ||")
    
def print_bottom_arrow():
    print("\\  /")
    print(" \\/")

def main():
    print_top_arrow()
    print_top_arrow()
    print_mid_arrow()
    print_top_arrow()
    print_bottom_arrow()
    print_top_arrow()

main()
